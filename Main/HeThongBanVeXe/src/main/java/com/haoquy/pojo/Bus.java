package com.haoquy.pojo;

/**
 *
 * @author HaoQuy
 */
public class Bus {

    
    private String id;
    private String from;
    private String to;
    private String time;
    private String date;
    public Bus(String id, String from,String to,String time,String date)
    {
        this.id = id;
        this.from = from;
        this.to = to;
        this.time = time;
        this.date = date;
    }
    public Bus()
    {
        
    }
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id.toUpperCase();
    }
        
    /**
     * @return the from
     */
    public String getFrom() {
        return from;
    }

    /**
     * @param from the from to set
     */
    public void setFrom(String from) {
        this.from = from;
    }

    /**
     * @return the to
     */
    public String getTo() {
        return to;
    }

    /**
     * @param to the to to set
     */
    public void setTo(String to) {
        this.to = to.toUpperCase();
    }

    /**
     * @return the time
     */
    public String getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(String time) {
        this.time = time;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }
}
